#!/usr/bin/env python3
"""
Write a type  function to concatenate two strings.
"""

def concat(str1: str, str2: str) -> str:
    """
    Concatenate two strings.

    Args:
        str1 (str): The first string.
        str2 (str): The second string.

    Returns:
        str:  concatenated two strings.
    """
    return str1 + str2

